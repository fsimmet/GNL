#include "libft.h"

size_t	ft_space(const char *s)
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return (0);
	while (s[i] == '\t' || s[i] == '\n' || s[i] == '\v' || s[i] == '\f' ||
			s[i] == '\r' || s[i] == ' ')
		i++;
	return (i);
}
